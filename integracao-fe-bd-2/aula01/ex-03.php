<?php


// Estrutura de repetição While
$cont = 1;

while ($cont <= 10) {
    echo ('Contando...');
    echo('<br>');


    // Incrementar o contador

    $cont++;
}


// // Estrutura de repetição Do

$cont = 1;

do {
    echo ('Contando com o Do...');
    echo('<br>');

    $cont++;

} while ($cont <= 10);


// // Estrutura de repetição For

for ($cont = 1; $cont <= 10; $cont++){

    echo ('Contando com o FOR');
    echo('<br>');
}

?>